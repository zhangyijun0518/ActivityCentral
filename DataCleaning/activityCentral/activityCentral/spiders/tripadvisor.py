    def start_requests(self):
        for url in self.urls:
            for d in self.dates:
                yield scrapy.Request(url=self.urls[0], callback=self.parse, meta={'dp': d}, dont_filter=True)
            
    def parse(self, response):
        item = ActivitycentralItem()
        self.data = self.get_json_data(response)
        print(self.data)
        if 'error' in self.data:
            return
        item['name'] = self.data['name']
        item['activity_date'] = self.datetime_to_utc(response.meta['dp']['date'])
        item['organizer'] = self.data['brand']['name']
        item['description'] = "<span>Let’s Roam is the #1 app-led scavenger hunt company. Walk to all the best landmarks and hidden gems, answering trivia questions and solving challenges. Work with your team or compete against them, as you learn new facts and create memorable experiences. Let’s Roam Scavenger Hunts are great as an everyday activity, or for bachelorette parties, birthday parties, corporate team building events and more! Each player chooses an interactive role, with challenges varying by person. </span>"
        item['location'] = self.get_location(response)[0]
        item['city'] = str(self.get_location(response)[1]).lower()
        # item['city'] = "san francisco"
        item['state'] = str(self.get_location(response)[2]).upper()
        # item['state'] = "CA"
        item['platform'] = 'Tripadvisor'
        item['rating'] = self.get_rating(response)
        item['tags'] = []
        item['source'] = response.request.url
        item['price'] = '$'+str(response.meta['dp']['price'])
        item['thumbnail'] = self.get_thumbnail(response)
        if len(item['thumbnail']) == 0:
            return
        return item
    
    def get_json_data(self, response):
        data_xpath = response.xpath("//script[contains(@type, 'application/ld+json')]/text()").extract()
        if len(data_xpath) == 0:
            return {'error': 'no data'}
        else:
            return json.loads(data_xpath[0].strip())
        
    def datetime_to_utc(self, dt):
        # date is a string
        timestamp = datetime.strptime(dt, '%Y-%m-%d').timestamp()
        timestamp += (24+7)*60*60-1
        return int(timestamp)
    
    def get_location(self, response):
        location_xpath = response.xpath("//div[contains(text(), 'Departure Point')]/following-sibling::div/span/text()").extract()
        if (len(location_xpath)) == 0:
            return ["You will get picked up", "", ""]
        path = location_xpath[0].split(",")
        street = ",".join(path[:-1])
        city = path[-3].strip()
        state = path[-2].strip().split(" ")[0]
        return [street, city, state]
    
    def get_rating(self, response):
        try:
            rating_xpath = response.xpath("//div[contains(text(), 'About the operator')]/..//svg/@aria-label").extract()
            return float(rating_xpath[0].split(" ")[0])
        except:
            return -1
    
    def get_thumbnail(self, response):
        return self.data['image']